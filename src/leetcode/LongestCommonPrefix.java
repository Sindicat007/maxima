package leetcode;

public class LongestCommonPrefix {
    public static void main(String[] args) {

    }

    public String longestCommonPrefix(String[] str) {
        if (str == null || str.length == 0) {
            return "";
        }
        for (int i = 0; i < str.length; i++){
            return str[i];
        }
        return "";
    }
}
